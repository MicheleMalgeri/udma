CREATE TABLE IF NOT EXISTS udma.user_info
(
    uid        INT NOT NULL AUTO_INCREMENT,
    age        INT,
    population INT,
    PRIMARY KEY (uid)
);

CREATE TABLE IF NOT EXISTS udma.user_weights
(
    uid   INT          NOT NULL,
    label VARCHAR(255) NOT NULL,
    value FLOAT        NOT NULL,
    PRIMARY KEY (uid, label),
    FOREIGN KEY (uid) REFERENCES udma.user_info (uid)
);

CREATE TABLE IF NOT EXISTS udma.labels
(
    label_name    VARCHAR(255) NOT NULL,
    is_active     INT          NOT NULL,
    is_primary    INT          NOT NULL,
    is_late       INT          NOT NULL,
    is_anag       INT          NOT NULL,
    question      VARCHAR(255) NOT NULL,
    user_question VARCHAR(255) NOT NULL,
    PRIMARY KEY (label_name)
);

CREATE TABLE IF NOT EXISTS udma.keywords
(
    keyword_name VARCHAR(255) NOT NULL,
    PRIMARY KEY (keyword_name)
);

CREATE TABLE IF NOT EXISTS udma.census
(
    city       VARCHAR(255) NOT NULL,
    population INT          NOT NULL
);

CREATE TABLE IF NOT EXISTS udma.mr_archive
(
    uda	VARCHAR(255),
    anno_ricovero VARCHAR(255),
    numero_ricovero	VARCHAR(255),
    regime VARCHAR(255),
    cognome VARCHAR(255),
    nome VARCHAR(255),
    sesso VARCHAR(255),
    data_nascita VARCHAR(255),
    comune_nascita VARCHAR(255),
    comune_residenza VARCHAR(255),
    codice_fiscale VARCHAR(255),
    data_ricovero VARCHAR(255),
    reparto_accettazione VARCHAR(255),
    tipo_ricovero VARCHAR(255),
    reparto_dimissione VARCHAR(255),
    data_dimissione VARCHAR(255),
    filename VARCHAR(255),
    file_content TEXT,
    ocr INT
);

